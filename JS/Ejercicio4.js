const altura= 10;
const base= 5;
console.log(`La altura del triangulo es: ${altura} cm`);
console.log(`La base del triangulo es: ${base} cm`);
const area= base*altura;
const perimetro= 2*(altura+base);
console.log(`El area del triangulo es: ${area} cm`);
console.log(`El perimetro del triangulo es: ${perimetro} cm`);
